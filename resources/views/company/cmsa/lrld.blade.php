            <p>何伟（1926年11月12日——2012年10月9日），原名郝荣甲，河北赵县人。著名经济学家，中国人民大学经济学教授，曾任中国人民大学《经济理论与经济管理》杂志主编，国家多个研究所特邀研究员及多所大学的客座教授，1989年曾被聘请到日本九州大学讲学，享受国务院政府特殊津贴。
何伟于抗日战争时期（1944年2月）参加革命工作，1951年考入中国人民大学财政系，1956年人民大学政治经济学研究生毕业后留校任教，长期从事经济学的教学和研究工作。先后担任政治经济学教研室副主任、党支部书记，《经济理论和经济管理》杂志主编，校理论小组成员，信息产业部电信经济专家委员会顾问等。主要著作有：《政治经济学新编》《商品经济与经济体制改革》《中国改革之研究》《现代企业战略学》《中国经济的非均衡发展》《何伟论文选》《分配经济学》《劳动力市场与劳动力商品》《观念撞击的火花》《邓小平理论：社会主义运动史上的第三次飞跃》《中国名经济学家论改革》《中国党政人士论经济改革》《中国名企业家论改革》《走向市场经济丛书》等专和主编及论文四百余篇，约500余万字。其中有两本书被《经济日报》评为“十佳经济作”，一本书获得1994年中国第八届图书奖。
上世纪80年代初期，人们用一些代名词来称呼那些杰出的经济学家们。比如，厉以宁教授被称为“厉股份”；吴敬琏教授被称为“吴市场”；萧灼基教授被称为“萧股市”；而何伟教授被称为“何商品”。之所以被称为“何商品”是因为他是最早提出“商品经济是达到完全社会主义社会不可逾越的阶段”的经济学家。</p>
