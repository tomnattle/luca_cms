            <p>于光远（1915年7月5日——2013年9月26日），上海人，我国著名的社会科学家、哲学家、经济学家，原中共中央顾问委员会委员，原国家科委副主任，中国社会科学院原副院长。
于光远原姓郁，名锺正，于光远是入党后起的名字。1935年参加一二九学生运动。1936年毕业于清华大学物理系。1937年初加入中国共产党。1941年起从事陕甘宁边区经济的研究工作，后在延安大学财经系任教。1948～1975年在中共中央宣传部工作。1955年被推选为中国科学院哲学社会科学学部的学部委员。1964年任国家科学委员会副主任。1975年以后任国家计划委员会经济研究所所长、中国社会科学院副院长兼马列主义毛泽东思想研究所所长、国家科委副主任、中共中央顾问委员会委员、中国社会科学院顾问、《中国大百科全书》总编委会副主任等职。
于光远长期从事经济研究工作，从上世纪八十年代起，致力于哲学、社会科学多学科的研究。发表的学术著作与政论很多。具有代表性的有：《政治经济学》（1952）,《论半社会主义的农业生产合作社的产品分配》（1955），《政治经济学社会主义部分探索》（1958），《政治经济学（资本主义部分）》（上、下册，1977—1978），《谈谈社会主义公有制和按劳分配问题》（1978），《论社会主义生产中的经济效果》（1978），《试论社会主义生产中的C、V、m》（1979），《经济、社会发展战略》（1982），《中国社会主义现代化建设丛书》（1982），《论我国的经济体制改革》（1985），《政治经济学社会主义部分探索》（1—5卷，1980—1991），《哲学论文演讲和笔记》，《一个哲学学派正在中国兴起》，《政治经济学社会主义部分探索·1—7卷》，《中国社会主义初级阶段的经济》，《经济社会发展战略》，《社会主义市场经济主体论杂记》，《我亲历的那次历史转折》，《文明的亚洲和亚洲的文明》，《评所谓人体特异功能》，《同伪科学至少还要斗一百年》，《我的教育思想》等，他还比较早地研究了社会主义初级阶段的经济的理论，著有《社会主义初级阶段的经济》(1988)一书。此书被经济学家评为“影响新中国经济的十本经济学著作”之一。
于光远学识渊博，学贯“自然科学和社会科学”，许多经济建设和经济体制改革中的重大理论问题都是由他率先或较早提出的，并参与了许多重要的决策。他是中国当代思想解放运动和改革开放的重要参与者和见证人。他不仅重视将马克思主义著作中已阐明的原理运用于现代经济生活，而且还十分重视如何将马克思主义运用于中国改革实践的经济理论发展。1978年召开的十一届三中全会掀开了中国历史的新篇章。于光远作为列席代表参加了这次著名的会议及之前的中央工作会议。而在中央工作会议上，邓小平作了那篇后来被认为是三中全会主题报告的著名讲话《解放思想，实事求是，团结一致向前看》，这篇讲话的手写提纲就一直保存在于光远那儿。
从1981年起，他又利用参与讨论中央文件的机会，多次主张将社会主义初级阶段的概念和基本特征写入中央文件。他的意见，发挥了一定的作用。可以说，于光远是中国当代思想解放运动和改革开放的重要参与者和见证人。</p>
